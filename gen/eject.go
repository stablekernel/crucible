package gen

import (
	"bytes"
	"fmt"
	"go/format"
	"sort"
	"strings"

	"github.com/stablekernel/crucible/state"
)

// behaviorKind discriminates the four registry slots a referenced behavior name
// can land in. The kinds are independent registries in the engine, so the same
// name may appear under more than one kind.
type behaviorKind int

const (
	kindGuard behaviorKind = iota
	kindAction
	kindAssign
	kindService
)

// names accumulates referenced behavior names per kind while the IR is walked.
// Each set is collapsed to a sorted, deduplicated slice before code is emitted so
// output is deterministic.
type names struct {
	guards   map[string]struct{}
	actions  map[string]struct{}
	assigns  map[string]struct{}
	services map[string]struct{}
}

func newNames() *names {
	return &names{
		guards:   map[string]struct{}{},
		actions:  map[string]struct{}{},
		assigns:  map[string]struct{}{},
		services: map[string]struct{}{},
	}
}

func (n *names) add(kind behaviorKind, name string) {
	if name == "" {
		return
	}
	switch kind {
	case kindGuard:
		n.guards[name] = struct{}{}
	case kindAction:
		n.actions[name] = struct{}{}
	case kindAssign:
		n.assigns[name] = struct{}{}
	case kindService:
		n.services[name] = struct{}{}
	}
}

// sortedSet returns the members of a set in ascending order.
func sortedSet(set map[string]struct{}) []string {
	out := make([]string, 0, len(set))
	for k := range set {
		out = append(out, k)
	}
	sort.Strings(out)
	return out
}

// Eject renders the given IR to typed Go stub source.
//
// Signature choice (a): the IR is walked directly. The type parameters S, E, and
// C are unused by the codegen — the IR's behavior references (state.Ref) and its
// context schema (state.ContextSchema) are non-generic, so the walk and the
// emitted source are driven entirely by those concrete shapes. The parameters
// remain on the signature so callers can pass an state.IR loaded from a typed
// machine without reflection or an interface wrapper, keeping Eject a drop-in over
// a typed machine's loaded IR.
//
// The returned bytes are a single gofmt'd Go source file. On a formatting failure
// (which indicates a codegen bug) the error wraps the unformatted source so it can
// be inspected.
func Eject[S comparable, E comparable, C any](ir state.IR[S, E, C], opts ...Option) ([]byte, error) {
	cfg := defaultConfig()
	for _, o := range opts {
		o(&cfg)
	}

	collected := newNames()
	for i := range ir.States {
		walkState(ir.States[i], collected)
	}

	// Build a deterministic identifier per (kind, name). A bare sanitized name is
	// used when it is unique across all kinds; otherwise the kind is suffixed so
	// the generated Go identifiers never collide while the registration string
	// stays the original name.
	idents := buildIdents(collected)

	var buf bytes.Buffer
	buf.WriteString("// Code generated by gen. DO NOT EDIT.\n\n")
	fmt.Fprintf(&buf, "package %s\n\n", cfg.packageName)

	// Imports are computed by the body builders; emit a placeholder, render the
	// body, then prepend the resolved import block.
	imports := newImportSet()
	imports.add("github.com/stablekernel/crucible/state")

	var body bytes.Buffer
	writeContextType(&body, ir.Context, cfg.contextTypeName, imports)
	writeStubs(&body, collected, idents, cfg.contextTypeName, imports)
	writeProvide(&body, collected, idents, cfg.contextTypeName)

	writeImports(&buf, imports)
	buf.Write(body.Bytes())

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		return nil, fmt.Errorf("gen: format generated source: %w\n--- unformatted ---\n%s", err, buf.String())
	}
	return formatted, nil
}

// walkState collects every behavior reference on a state and recurses into its
// children, regions, and (via transitions) its outgoing edges.
func walkState[S comparable, E comparable, C any](s state.State[S, E, C], n *names) {
	for _, r := range s.OnEntry {
		n.add(kindAction, r.Name)
	}
	for _, r := range s.OnExit {
		n.add(kindAction, r.Name)
	}
	for _, r := range s.OnDone {
		n.add(kindAction, r.Name)
	}
	for _, r := range s.OnEntryAssign {
		n.add(kindAssign, r.Name)
	}
	for _, r := range s.OnExitAssign {
		n.add(kindAssign, r.Name)
	}
	for _, inv := range s.Invoke {
		n.add(kindService, inv.Src.Name)
	}
	for i := range s.Transitions {
		walkTransition(s.Transitions[i], n)
	}
	for i := range s.Children {
		walkState(s.Children[i], n)
	}
	for i := range s.Regions {
		for j := range s.Regions[i].States {
			walkState(s.Regions[i].States[j], n)
		}
	}
}

// walkTransition collects the guard, action, and assign references on one edge.
func walkTransition[S comparable, E comparable, C any](t state.Transition[S, E, C], n *names) {
	for _, r := range t.Guards {
		n.add(kindGuard, r.Name)
	}
	for _, r := range t.Effects {
		n.add(kindAction, r.Name)
	}
	for _, r := range t.Assigns {
		n.add(kindAssign, r.Name)
	}
}

// identKey pairs a kind with the original name for indexing the ident map.
type identKey struct {
	kind behaviorKind
	name string
}

// buildIdents derives a unique, deterministic Go identifier for every collected
// (kind, name). The base identifier is the sanitized name; when that base is
// shared by more than one (kind, name) pair the kind is appended so the emitted
// function identifiers stay unique. The original name is always preserved as the
// registration string elsewhere.
func buildIdents(n *names) map[identKey]string {
	type entry struct {
		key  identKey
		base string
	}
	var entries []entry
	collect := func(kind behaviorKind, set map[string]struct{}) {
		for _, name := range sortedSet(set) {
			entries = append(entries, entry{key: identKey{kind, name}, base: behaviorIdent(name)})
		}
	}
	collect(kindGuard, n.guards)
	collect(kindAction, n.actions)
	collect(kindAssign, n.assigns)
	collect(kindService, n.services)

	baseCount := map[string]int{}
	for _, e := range entries {
		baseCount[e.base]++
	}

	out := make(map[identKey]string, len(entries))
	used := map[string]struct{}{}
	for _, e := range entries {
		id := e.base
		if baseCount[e.base] > 1 {
			id = e.base + kindSuffix(e.key.kind)
		}
		// Guard against any residual collision (e.g. two original names that
		// sanitize to the same base within one kind) by appending an ordinal.
		base := id
		for i := 1; ; i++ {
			if _, taken := used[id]; !taken {
				break
			}
			id = fmt.Sprintf("%s%d", base, i)
		}
		used[id] = struct{}{}
		out[e.key] = id
	}
	return out
}

func kindSuffix(k behaviorKind) string {
	switch k {
	case kindGuard:
		return "Guard"
	case kindAction:
		return "Action"
	case kindAssign:
		return "Assign"
	case kindService:
		return "Service"
	default:
		return ""
	}
}

// writeStubs emits one panic-bodied stub per collected behavior, typed to the
// exact engine signature with the context type substituted for the engine's C.
func writeStubs(buf *bytes.Buffer, n *names, idents map[identKey]string, ctxName string, imports *importSet) {
	for _, name := range sortedSet(n.guards) {
		id := idents[identKey{kindGuard, name}]
		fmt.Fprintf(buf, "func %s(ctx state.GuardCtx[%s]) bool {\n\tpanic(%q)\n}\n\n",
			id, ctxName, "TODO: implement guard "+name)
	}
	for _, name := range sortedSet(n.actions) {
		id := idents[identKey{kindAction, name}]
		fmt.Fprintf(buf, "func %s(ctx state.ActionCtx[%s]) (state.Effect, error) {\n\tpanic(%q)\n}\n\n",
			id, ctxName, "TODO: implement action "+name)
	}
	for _, name := range sortedSet(n.assigns) {
		id := idents[identKey{kindAssign, name}]
		fmt.Fprintf(buf, "func %s(in state.AssignCtx[%s]) %s {\n\tpanic(%q)\n}\n\n",
			id, ctxName, ctxName, "TODO: implement assign "+name)
	}
	if len(n.services) > 0 {
		imports.add("context")
	}
	for _, name := range sortedSet(n.services) {
		id := idents[identKey{kindService, name}]
		fmt.Fprintf(buf, "func %s(ctx context.Context, in state.ServiceCtx[%s]) (any, error) {\n\tpanic(%q)\n}\n\n",
			id, ctxName, "TODO: implement service "+name)
	}
}

// writeProvide emits the Provide function registering every stub against a
// state.Registry by its ORIGINAL name. Assigns register through Reducer (the
// engine's name for the assign registry slot).
func writeProvide(buf *bytes.Buffer, n *names, idents map[identKey]string, ctxName string) {
	fmt.Fprintf(buf, "// Provide registers every generated stub against reg by its IR name and returns\n")
	fmt.Fprintf(buf, "// reg for chaining.\n")
	fmt.Fprintf(buf, "func Provide(reg *state.Registry[%s]) *state.Registry[%s] {\n", ctxName, ctxName)
	for _, name := range sortedSet(n.guards) {
		fmt.Fprintf(buf, "\treg.Guard(%q, %s)\n", name, idents[identKey{kindGuard, name}])
	}
	for _, name := range sortedSet(n.actions) {
		fmt.Fprintf(buf, "\treg.Action(%q, %s)\n", name, idents[identKey{kindAction, name}])
	}
	for _, name := range sortedSet(n.assigns) {
		fmt.Fprintf(buf, "\treg.Reducer(%q, %s)\n", name, idents[identKey{kindAssign, name}])
	}
	for _, name := range sortedSet(n.services) {
		fmt.Fprintf(buf, "\treg.Service(%q, %s)\n", name, idents[identKey{kindService, name}])
	}
	buf.WriteString("\treturn reg\n}\n")
}

// importSet collects import paths and renders them as a sorted block.
type importSet struct {
	paths map[string]struct{}
}

func newImportSet() *importSet { return &importSet{paths: map[string]struct{}{}} }

func (s *importSet) add(path string) { s.paths[path] = struct{}{} }

func writeImports(buf *bytes.Buffer, s *importSet) {
	if len(s.paths) == 0 {
		return
	}
	paths := make([]string, 0, len(s.paths))
	for p := range s.paths {
		paths = append(paths, p)
	}
	sort.Strings(paths)
	if len(paths) == 1 {
		fmt.Fprintf(buf, "import %q\n\n", paths[0])
		return
	}
	buf.WriteString("import (\n")
	for _, p := range paths {
		fmt.Fprintf(buf, "\t%q\n", p)
	}
	buf.WriteString(")\n\n")
}

// behaviorIdent derives the Go function identifier for a behavior stub from its
// IR name. When the name is already a valid Go identifier it is used verbatim, so
// a name like "isPaid" or "g" maps to the same-cased func — the stub reads as the
// behavior it stands for. A name with separators (snake_case, kebab-case, dotted
// path) or a leading digit is camel-cased and prefixed via sanitizeIdent so the
// result is always valid. The mapping is deterministic.
func behaviorIdent(name string) string {
	if isGoIdent(name) {
		return name
	}
	return sanitizeIdent(name)
}

// isGoIdent reports whether name is already a non-empty valid Go identifier (ASCII
// letters/underscore start, ASCII alphanumerics/underscore tail). Keyword
// collisions are not a concern for callable identifiers here.
func isGoIdent(name string) bool {
	if name == "" {
		return false
	}
	for i, r := range name {
		isLetter := r == '_' || (r >= 'a' && r <= 'z') || (r >= 'A' && r <= 'Z')
		isDigit := r >= '0' && r <= '9'
		if i == 0 && !isLetter {
			return false
		}
		if i > 0 && !isLetter && !isDigit {
			return false
		}
	}
	return true
}

// sanitizeIdent turns an arbitrary IR name into a safe, exported Go identifier.
// Non-identifier runes act as word separators (snake_case, kebab-case, and dotted
// paths all split into camel-cased words); a leading digit is prefixed so the
// result is always a valid identifier. The mapping is deterministic.
func sanitizeIdent(name string) string {
	var words []string
	var cur strings.Builder
	flush := func() {
		if cur.Len() > 0 {
			words = append(words, cur.String())
			cur.Reset()
		}
	}
	for _, r := range name {
		switch {
		case r >= 'a' && r <= 'z', r >= 'A' && r <= 'Z', r >= '0' && r <= '9':
			cur.WriteRune(r)
		default:
			flush()
		}
	}
	flush()

	var b strings.Builder
	for _, w := range words {
		b.WriteString(strings.ToUpper(w[:1]) + w[1:])
	}
	ident := b.String()
	if ident == "" {
		return "Behavior"
	}
	if c := ident[0]; c >= '0' && c <= '9' {
		ident = "X" + ident
	}
	return ident
}
