// order is the representative context for the test machine. Its fields span
// several SchemaKinds (string, int, bool, duration, time, list, map) so the
// generated context type exercises the full kind->Go mapping.
type order struct {
	ID       string         `json:"id"`
	Total    int            `json:"order_total"`
	Paid     bool           `json:"paid"`
	Window   time.Duration  `json:"window"`
	PlacedAt time.Time      `json:"placed_at"`
	Items    []string       `json:"items"`
	Extras   map[string]int `json:"extras"`
}

// buildIR constructs a representative machine via the real DSL and round-trips it
// through JSON to obtain a *state.IR — the same path a host's eject tool would
// take from a persisted machine definition. The machine references two guards, two
// actions, one assign, and one invoked service.
func buildIR(t *testing.T) state.IR[string, string, order] {
	t.Helper()

	m := state.ForgeFor[order]("orders").
		WithContextSchema(state.SchemaOf[order]()).
		Guard("isPaid", func(c state.GuardCtx[order]) bool { return c.Entity.Paid }).
		Guard("hasItems", func(c state.GuardCtx[order]) bool { return len(c.Entity.Items) > 0 }).
		Action("notify", func(state.ActionCtx[order]) (state.Effect, error) { return nil, nil }).
		Action("logEntry", func(state.ActionCtx[order]) (state.Effect, error) { return nil, nil }).
		Reducer("applyTotal", func(in state.AssignCtx[order]) order { return in.Entity }).
		Service("charge", func(context.Context, state.ServiceCtx[order]) (any, error) { return nil, nil }).
		State("cart").
		OnEntry("logEntry").
		Transition("cart").On("checkout").GoTo("paying").
		When("hasItems").
		Assign("applyTotal").
		State("paying").
		Invoke("charge", state.WithInvokeOnDone("paid"), state.WithInvokeOnError("failed")).
		Transition("paying").On("paid").GoTo("done").
		When("isPaid").
		Do("notify").
		State("done").
		Initial("cart").
		Quench()

	raw, err := m.ToJSON()
	if err != nil {
		t.Fatalf("ToJSON: %v", err)
	}
	ir, err := state.LoadFromJSON[string, string, order](raw)
	if err != nil {
		t.Fatalf("LoadFromJSON: %v", err)
	}
	return *ir
}

func TestEject_GeneratesExpectedSurface(t *testing.T) {
	src, err := Eject(buildIR(t))
	if err != nil {
		t.Fatalf("Eject: %v", err)
	}
	got := string(src)

	mustContain := []string{
		"// Code generated by gen. DO NOT EDIT.",
		"package machine",
		"type Context struct {",
		"func isPaid(ctx state.GuardCtx[Context]) bool",
		"func hasItems(ctx state.GuardCtx[Context]) bool",
		"func notify(ctx state.ActionCtx[Context]) (state.Effect, error)",
		"func logEntry(ctx state.ActionCtx[Context]) (state.Effect, error)",
		"func applyTotal(in state.AssignCtx[Context]) Context",
		"func charge(ctx context.Context, in state.ServiceCtx[Context]) (any, error)",
		`reg.Guard("isPaid", isPaid)`,
		`reg.Action("notify", notify)`,
		`reg.Reducer("applyTotal", applyTotal)`,
		`reg.Service("charge", charge)`,
		"func Provide(reg *state.Registry[Context]) *state.Registry[Context]",
	}
	for _, want := range mustContain {
		if !strings.Contains(got, want) {
			t.Errorf("generated source missing %q\n---\n%s", want, got)
		}
	}
}

// collapseWS reduces every run of whitespace to a single space so gofmt's column
// alignment of struct fields does not defeat substring assertions.
func collapseWS(s string) string {
	return strings.Join(strings.Fields(s), " ")
}

func TestEject_ContextFieldMapping(t *testing.T) {
	src, err := Eject(buildIR(t))
	if err != nil {
		t.Fatalf("Eject: %v", err)
	}
	got := collapseWS(string(src))
	mustContain := []string{
		"Id string `json:\"id\"`",
		"OrderTotal int64 `json:\"order_total\"`",
		"Paid bool `json:\"paid\"`",
		"Window time.Duration `json:\"window\"`",
		"PlacedAt time.Time `json:\"placed_at\"`",
		"Items []any `json:\"items\"`",
		"Extras map[string]any `json:\"extras\"`",
	}
	for _, want := range mustContain {
		if !strings.Contains(got, want) {
			t.Errorf("context field mapping missing %q\n---\n%s", want, string(src))
		}
	}
}

func TestEject_Deterministic(t *testing.T) {
	ir := buildIR(t)
	a, err := Eject(ir)
	if err != nil {
		t.Fatalf("Eject a: %v", err)
	}
	b, err := Eject(ir)
	if err != nil {
		t.Fatalf("Eject b: %v", err)
	}
	if !bytes.Equal(a, b) {
		t.Fatalf("Eject is not deterministic:\n--- a ---\n%s\n--- b ---\n%s", a, b)
	}
}

