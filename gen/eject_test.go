// TestEject_CompilesAndWires writes the generated source into a fresh temp module
// and runs `go build ./...` against the real state module via a replace directive.
// A clean build proves every stub carries the exact engine signature and that
// Provide type-checks against a real *state.Registry[Context].
func TestEject_CompilesAndWires(t *testing.T) {
	src, err := Eject(buildIR(t))
	if err != nil {
		t.Fatalf("Eject: %v", err)
	}

	// Resolve the sibling state module relative to this package so the proof
	// works from any checkout; the build runs with cwd at the gen package dir.
	stateAbs, err := filepath.Abs("../state")
	if err != nil {
		t.Fatalf("resolve state module path: %v", err)
	}

	tmp := t.TempDir()
	gomod := "module genout\n\ngo 1.25.11\n\nrequire github.com/stablekernel/crucible/state v0.0.0\n\nreplace github.com/stablekernel/crucible/state => " + stateAbs + "\n"
	writeFile(t, filepath.Join(tmp, "go.mod"), gomod)
	writeFile(t, filepath.Join(tmp, "gen.go"), string(src))

	// wire.go drives Provide against a real registry, proving the generated
	// Provide type-checks end to end.
	wire := "package machine\n\nimport \"github.com/stablekernel/crucible/state\"\n\nfunc wire() *state.Registry[Context] {\n\treturn Provide(state.NewRegistry[Context]())\n}\n\nvar _ = wire\n"
	writeFile(t, filepath.Join(tmp, "wire.go"), wire)

	cmd := exec.Command("go", "build", "./...")
	cmd.Dir = tmp
	cmd.Env = append(os.Environ(), "GOWORK=off", "GOFLAGS=-mod=mod")
	out, err := cmd.CombinedOutput()
	if err != nil {
		t.Fatalf("temp module build failed: %v\n%s\n--- generated ---\n%s", err, out, src)
	}
}

func writeFile(t *testing.T, path, content string) {
	t.Helper()
	if err := os.WriteFile(path, []byte(content), 0o644); err != nil {
		t.Fatalf("write %s: %v", path, err)
	}
}
